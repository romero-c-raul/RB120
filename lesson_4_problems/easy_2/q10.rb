# What are the benefits of using Object Oriented Programming in Ruby? 
# Think of as many as you can.

=begin
1.  It allows us to have many small independent parts working together, instead of
    a lot of parts depending on each other; small changes don't affect the program
    as a whole
2.  Hides pieces of functionality; code cannot be manipulated or transformed without
    obvious intention
3.  The ability to have different types of data respond to a common interface
4.  OOP gives us flexibility, allowing us to use the same code for new purposes
4.  Cleaner code: inhertance allows us to not repeat ourselves.

# Launch School answer:
Because there are so many benefits to using OOP we will just summarize some of 
the major ones:

1.  Creating objects allows programmers to think more abstractly about the code 
    they are writing.
2.  Objects are represented by nouns so are easier to conceptualize.
3.  It allows us to only expose functionality to the parts of code that need it, 
    meaning namespace issues are much harder to come across.
4.  It allows us to easily give functionality to different parts of an application 
    without duplication.
5.  We can build applications faster as we can reuse pre-written code.
6.   As the software becomes more complex this complexity can be more easily managed.

=end